int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	t;
	
	i = 0;
	t = 0;
	while(s1[i] != '\0')
	{
		i++;
	}
	while(s2[t] != '\0')
	{
		t++;
	}
	return (i - t);
}


