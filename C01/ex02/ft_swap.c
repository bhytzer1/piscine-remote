void	ft_swap(int *a, int *b)
{
	int	c;

	c = *a;
	*a = *b;
	*b = c;
}

int	main(void)
{
	int *a = 4;
	int *b = 5;
	printf("a: %d b: %d", a, b); 
	ft_swap(a, b);
	printf("a: %d b: %d", a, b); 
}
